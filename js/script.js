function createCard(event, dept = null){

    let watermarkText = "";

    if(dept && departmentWatermarks[dept]){
        watermarkText = `<span class="back-watermark">${departmentWatermarks[dept]}</span>`;
    }

    return `
    <div class="card" onclick="this.classList.toggle('flip')">
        <div class="card-inner">

            <div class="card-front">
                <h3 class="event-title">${event.name}</h3>
                <div class="gif-container">
                    <img src="assets/gifs/${event.gif}" alt="${event.name}">
                </div>
                <p class="click-hint">Click for more details</p>
            </div>

            <div class="card-back">
                
                <h4 class="event-back-title">${event.name}</h4>

                <p class="event-description">
                    ${event.description}
                </p>

                <div class="event-meta">
                    <div class="meta-item">
                        <i class="fa-regular fa-calendar"></i>
                        <span>${event.time}</span>
                    </div>

                    <div class="meta-item">
                        <i class="fa-solid fa-location-dot"></i>
                        <span>${event.venue}</span>
                    </div>

                    <div class="meta-item">
                        <i class="fa-solid fa-users"></i>
                        <span>${event.participation}</span>
                    </div>
                </div>
                ${event.themes ? `
                    <div class="themes-section">
                        <div class="themes-toggle" onclick="toggleThemes(event, this)">
                            <i class="fa-solid fa-layer-group"></i>
                            <span>View Themes</span>
                        </div>

                        <ul class="themes-list">
                            ${event.themes.map(theme => `<li>${theme}</li>`).join("")}
                        </ul>
                    </div>
                ` : ""}
            </div>



        </div>
    </div>
    `;
}

