function toggleThemes(e, element){
    e.stopPropagation();  // 🔥 prevents card flip

    const list = element.nextElementSibling;
    list.classList.toggle("show");
}


function loadDepartment(dept){
    const container = document.getElementById("events-container");
    container.innerHTML = "";

    technicalEvents[dept].forEach(event=>{
        container.innerHTML += createCard(event, dept);
    });
}

function loadCultural(){
    const container=document.getElementById("cultural-container");
    culturalEvents.forEach(ev=>{
        container.innerHTML+=createCard(ev);
    });
}

/* TAB CLICK LOGIC */
document.querySelectorAll(".dept-tabs button").forEach(button=>{
    button.addEventListener("click", function(){

        document.querySelectorAll(".dept-tabs button")
            .forEach(btn=>btn.classList.remove("active"));

        this.classList.add("active");

        const dept = this.getAttribute("data-dept");
        loadDepartment(dept);
    });
});

/* DEFAULT LOAD → CSE */
window.addEventListener("DOMContentLoaded", ()=>{
    loadDepartment("cse");
    loadCultural();
});

